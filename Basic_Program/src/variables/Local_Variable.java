package variables;

public class Local_Variable {
	public static void main(String[] args) {
		add();
		mutiply();
		
	}
	public static void add() {
		int a =  4000;
		int b = 5000;
		System.out.println("The sum of two variable is " + (a + b));
	}
	public static void mutiply() {
		int x = 12;
		int y = 5;
		System.out.println("The multiplication of two number is " + (x * y));
	}

}
